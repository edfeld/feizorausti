    /**
     * Retrieves the next chapter, or returns false if there is none
     *
     * @param string $currentChapter the key of the current chapter
     * @return string the key of the next chapter or false if there is none
     */
    public function getNextChapter($currentChapter) {
        if (isset($this->list[$currentChapter]))
            return $this->list[$currentChapter];
        else
            return false;
    }

    /**
     * Retrieves the previous chapter, or returns false if there is none
     *
     * @param string $currentChapter the key of the current chapter
     * @return string the key of the previous chapter or false if there is none
     */
    public function getPrevChapter($currentChapter) {
        $found = false;
        foreach ($this->list as $key => $value) {
            if ($value == $currentChapter) {
                $found = $key;
                break;
            }
        }

        return $found;
    }
